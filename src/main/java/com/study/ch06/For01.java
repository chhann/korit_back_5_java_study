package com.study.ch06;

public class For01 {
    public static void main(String[] args) {

        // for 문은 반복횟수가 중심이다
        for(int i = 0; i < 10; i++) {
            System.out.println("i: " +i);
        }

        int i = 0;
        // while 문은 조건이 중심이다
        while(i < 10 ) {
            System.out.println("i: " + i);
            i++;
        }

    }
}
